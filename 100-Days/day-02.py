# Subscripting
print("Hello"[0])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123_456_789)

# Float = Floating Print Number
print(3.14159)

# Boolean
print(True)
print(False)

# Type Checking using - type() function
print(type("Hello"))

# TypeCasting
print(int("123") + int("345"))

